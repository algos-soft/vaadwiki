public class WrapQuattroStringhe {

    private String prima;

    private String seconda;

    private String terza;

    private String quarta;


    public WrapQuattroStringhe(String prima, String seconda, String terza, String quarta) {
        this.prima = prima;
        this.seconda = seconda;
        this.terza = terza;
        this.quarta = quarta;
    }


    public String getPrima() {
        return prima;
    }


    public String getSeconda() {
        return seconda;
    }


    public String getTerza() {
        return terza;
    }


    public String getQuarta() {
        return quarta;
    }

}
